namespace NWkHtmlToX.Settings {

    /// <summary>
    /// This enum type is used to specify each page's orientation
    /// </summary>
    public enum Orientation {

        /// <summary>
        /// the page's height is greater than its width
        /// </summary>
        Portrait = 0,

        /// <summary>
        /// the page's width is greater than its height
        /// </summary>
        Landscape = 1
    }
}